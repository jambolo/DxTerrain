class SkirtIB : public boost::noncopyable
{
public:
	//! Constructor
	SkirtIB( IDirect3DIndexBuffer9 * pIB = 0 ) : m_pIB( pIB ) {}

	//! Destructor
	~SkirtIB() { if ( m_pIB != 0 ) m_pIB->Release(); }

	IDirect3DIndexBuffer9 *		m_pIB;
};


